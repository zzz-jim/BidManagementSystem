        // POST: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            // This doen't count login failures towards lockout only two factor authentication
            // To enable password failures to trigger lockout, change to shouldLockout: true
            var result = await SignInManager.PasswordSignInAsync(model.UserName, model.Password, model.RememberMe, shouldLockout: false);
            switch (result)
            {
                case SignInStatus.Success:

                    // Cache the login user name and secion in the session.
                    var user = await UserManager.FindByNameAsync(model.UserName);

                    if (user != null)
                    {
                        var section = await SectionManager.GetUserSectionsAsync(user.Id);
                        var userGroups = await this.GroupManager.GetUserGroupsAsync(user.Id);
                        //拿到登录人的角色
                        string allRoles = "";
                        using (ApplicationDbContext userManager = new ApplicationDbContext())
                        {
                            var rolesList = userManager.Users.FirstOrDefault(p => p.Id == user.Id).Roles;
                            

                            foreach (var itme in rolesList)
                            {
                                allRoles =allRoles+","+ userManager.Roles.Where(p=>p.Id==itme.RoleId).FirstOrDefault().Name;
                            }
                            
                        }
                       
                        ISession session = new SessionManager();
                        session[SessionKeyEnum.UserModel] = model.UserName;
                        // TODO: 现在只处理一个用户只有一个seciton的情况
                        if (section.Count() > 0)
                        {
                            session[SessionKeyEnum.SectionName] = section.FirstOrDefault().Name;
                            session[SessionKeyEnum.SectionId] = section.FirstOrDefault().Id;
                            session[SessionKeyEnum.UserRoles] = allRoles;
                        }
                    }

                    return RedirectToLocal(returnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.RequiresVerification:
                    return RedirectToAction("SendCode", new { ReturnUrl = returnUrl });
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Invalid login attempt.");
                    ViewBag.LoginSuccess = false;
                    return View(model);
            }
        }

        //
        // GET: /Account/VerifyCode
        [AllowAnonymous]
        public async Task<ActionResult> VerifyCode(string provider, string returnUrl)
        {
            // Require that the user has already logged in via username/password or external login
            if (!await SignInManager.HasBeenVerifiedAsync())
            {
                return View("Error");
            }
            var user = await UserManager.FindByIdAsync(await SignInManager.GetVerifiedUserIdAsync());
            if (user != null)
            {
                ViewBag.Status = "For DEMO purposes the current " + provider + " code is: " + await UserManager.GenerateTwoFactorTokenAsync(user.Id, provider);
            }
            return View(new VerifyCodeViewModel { Provider = provider, ReturnUrl = returnUrl });
        }

        //
        // POST: /Account/VerifyCode
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> VerifyCode(VerifyCodeViewModel model)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            var result = await SignInManager.TwoFactorSignInAsync(model.Provider, model.Code, isPersistent: false, rememberBrowser: model.RememberBrowser);
            switch (result)
            {
                case SignInStatus.Success:
                    return RedirectToLocal(model.ReturnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Invalid code.");
                    return View(model);
            }
        }

